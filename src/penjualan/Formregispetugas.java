    void tampil()
{
    tm=new DefaultTableModel(null,spt);
    try
    {
        kj.connect();
        kj.rs=kj.st.executeQuery("select * from petugas");
        while (kj.rs.next())
        {
            String a=kj.rs.getString("kd_petugas");
            String b=kj.rs.getString("nm_petugas");
            String c=kj.rs.getString("jenkel");
            String d=kj.rs.getString("umur_petugas");
            String e=kj.rs.getString("alamat_petugas");
           // String f=kj.rs.getString("password");
            //String g=kj.rs.getString("stok");
            
            String[] data={a,b,c,d,e};
            tm.addRow(data);
            
        }
        tb_petugas.setModel(tm);
    }
    catch (Exception e)
    {
        JOptionPane.showMessageDialog(null, "Tabel belum muncul" +e);
    }
}
    
