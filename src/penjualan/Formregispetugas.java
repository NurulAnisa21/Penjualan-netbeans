         void no_auto()
    {
        try
        {
            int kode;
            kj.rs=kj.st.executeQuery("select right(kd_petugas,3) as x from petugas order by kd_petugas desc");
            if (kj.rs.next())
            {
                kode = Integer.parseInt(kj.rs.getString("x"))+1;
                kdpetugas.setText("PTG00"+Integer.toString(kode));
            }
            else
            {
                kode=1;
                kdpetugas.setText("PTG00"+Integer.toString(kode));
                
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Kode tidak bisa tampil"+e);
        }
    }

